package com.projecthub.base.team.domain.enums;

/**
 * Represents the current status of a team in the system.
 */
public enum TeamStatus {
    /**
     * Team is currently active and operational
     */
    ACTIVE,
    /**
     * Team is temporarily inactive
     */
    INACTIVE,
    /**
     * Team has been archived and is no longer in use
     */
    ARCHIVED
}